	/**
	 * ルートドメイン数のカウント＆更新
	 *
	 * @param  array()
	 * @param  int
	 * @return char
	 */
	public static function get_rootdomain_chg($cl_seq, $rootdomain)
	{

		$CI =& get_instance();

		$CI->load->model('Keyword',    'kw', TRUE);
		$CI->load->model('Rootdomain', 'rd', TRUE);
// 		$CI->load->library('lib_rootdomain');

		$set_data_kw['kw_cl_seq']      = $cl_seq;
		$set_data_kw['kw_rootdomain']  = $rootdomain;

		$get_rd_cnt = $CI->kw->get_rootdomain_cnt($set_data_kw);

		// ルートドメイン管理データの作成
		$set_data_rd['rd_cl_seq']      = $cl_seq;
		$set_data_rd['rd_rootdomain']  = $rootdomain;
		$set_data_rd['rd_keyword_cnt'] = $get_rd_cnt;

		$get_rd_data = $CI->rd->check_rootdomain($set_data_rd);
		if (empty($get_rd_data))
		{
			// INSERT
			$CI->rd->insert_rootdomain($set_data_rd);
		} else {
			$set_data_rd['rd_seq'] = $get_rd_data[0]['rd_seq'];

			// UPDATE
			$CI->rd->update_rootdomain($set_data_rd);
		}

	}

	/**
	 * ルートドメインの削除＆有無チェック
	 *
	 * @param  array()
	 * @param  int
	 * @return char
	 */
	public static function get_rootdomain_del($cl_seq, $rootdomain)
	{

		$CI =& get_instance();

		$CI->load->model('Keyword',    'kw', TRUE);
		$CI->load->model('Rootdomain', 'rd', TRUE);
		$CI->load->library('lib_rootdomain');

		$set_data_kw['kw_cl_seq']      = $cl_seq;
		$set_data_kw['kw_rootdomain']  = $rootdomain;

		$get_rd_cnt = $CI->kw->get_rootdomain_cnt($set_data_kw);


// 		print($get_rd_cnt);
// 		exit;


		if ($get_rd_cnt === 0)
		{

			// DELETE
			$CI->rd->delete_rootdomain(NULL, $cl_seq, $rootdomain);

		} else {

			// ルートドメイン数のカウント＆更新
			$CI->lib_rootdomain->get_rootdomain_chg($cl_seq, $rootdomain);

		}

	}

	/*

	与えられたドメイン名かURLから、ルートドメインとTLD（ccSLD含む）を抽出する。
	IDNドメインは事前にpunycodeに変換する。

	## 関数名

	rootdomain

	## 要求

	URL or ドメイン名

	## 返り値

	配列形式

	キー名：
	[request]		リクエスト文字列
	[host]			ホスト名
	[rootdomain]	ルートドメイン
	[tld]			TLD

	## TLDリスト配列作成手順
	１．リストダウンロード https://publicsuffix.org/list/
	２．コメントアウト除去
	３．文字数降順に並び替え
	４．!否定行削除
	５．punycode変換
	６．*アスタリスクを[a-z0-9-]+に置換
	７．.ドットに\エスケープ追加

	*/
    public static function get_rootdomain($val)
    {

    	set_time_limit(0);

    	$CI =& get_instance();
//     	$CI->load->library('lib_rootdomain');

    	// TLDリストをセット
    	$tld_array = $CI->lib_rootdomain->_set_tld_list();

    	//URL → HOST
    	$host = preg_replace("/https?:\/\/|\/(.*)/i", "", $val);

    	//TLD MATCHING
    	foreach ($tld_array as $tld)
    	{

    		if (preg_match("/([a-z0-9-]+\.($tld))$/i", $host, $match))
    		{

    			$result['rootdomain'] = $match[1];
    			$result['tld']        = $match[2];

    			break;

    		}

    	}

    	//LOOP-MATHING NG
    	if (empty($result['tld']))
    	{

    		if (preg_match("/([a-z0-9-]+\.([a-z0-9-]+))$/i", $host, $match))
    		{

    			$result['rootdomain'] = $match[1];
    			$result['tld']        = $match[2];

    		}

    	}

    	$result['request'] = $val;
    	$result['host']    = $host;

    	return $result;

    }

